#include<bits/stdc++.h>
using namespace std;
int main()
{
    int num;
    cin>>num;
    switch(num){
    case 1:
        cout<<"one"<<endl;
        break;
    case 2:
        cout<<"two"<<endl;
        break;
    case 3:
        cout<<"three"<<endl;
        break;
    default:
        cout<<"Other digit"<<endl;
    }
    return 0;
}
